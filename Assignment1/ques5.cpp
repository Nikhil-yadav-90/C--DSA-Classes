//Ques: WAP to find the circumference of a circle with radius 10 in C++.


#include <iostream>
using namespace std;
int main(){
    float radius = 10;
    float PI = 3.14;
    float circum = 2 * PI * radius;
    cout<<"The value of circumference of circle with radius 10 is :"<<circum<<endl;
    return 0;
}